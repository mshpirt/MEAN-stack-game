angular.module('playerFightModule', ['playerFightService']).controller('playerFightController', function($scope, $http, playerFightData) 
	{

    // $scope.tagline = 'pls';

    // $scope.playername = playerFightData.get();
    // $scope.selectedPlayer = playerFightData.myProperty

    $http.get('playerfetcherRandom')
    .success(function (response) 
      {

      $scope.tagline = "Generated by playerfightCtrl.js";

      $scope.randomPlayer = response;
      // console.log($scope.randomPlayer[0].stats.macro);

      $scope.selectedPlayer = playerFightData.myProperty;
      // console.log($scope.selectedPlayer.stats.macro);

      $scope.selectedPlayerScore = $scope.selectedPlayer.stats.macro + $scope.selectedPlayer.stats.luck;
      // console.log($scope.selectedPlayerScore);
      $scope.randomPlayerScore = $scope.randomPlayer[0].stats.macro + $scope.randomPlayer[0].stats.luck;
      // console.log($scope.randomPlayerScore);

      if ($scope.selectedPlayerScore > $scope.randomPlayerScore)
      	{
      	$scope.winner = $scope.selectedPlayer.playername;
      	}

      else if ($scope.selectedPlayerScore < $scope.randomPlayerScore)
      	{
      	$scope.winner = $scope.randomPlayer[0].playername;
      	}

      else if ($scope.selectedPlayerScore == $scope.randomPlayerScore)
      	{
      	$scope.winner = "It's a tie!"
      	}

      });
      

    // do this in directive later maybe
    // $scope

	});